package Projects.Operators;

public class Operators1 {
    public static void main(String[] args) {
        int x = 5;
        int y = 10;
        int z = 11;
        System.out.println(x + y);
        System.out.println(y - x);
        System.out.println(x * y);
        System.out.println(y / x);
        System.out.println(z % x);
        System.out.println(++x);
        System.out.println(--y);

    }
}
