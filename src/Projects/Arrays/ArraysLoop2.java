public class ArraysLoop2 {
    public static void main(String[] args) {
        //Loop Through an Array with For-Each
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        for (String i : cars) {
            System.out.println(i);
        }

    }
}
