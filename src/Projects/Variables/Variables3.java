package Projects.Variables;

public class Variables3 {
    public static void main(String[] args) {

        int x = 5;
        int y = 6;
        int z = 50;
        System.out.println(x + y + z);

        //or
        int a = 5, b = 6, c = 50;
        System.out.println(a + b + c);

        //or

        int d, e, f;
        d = e = f = 50;
        System.out.println(d + e + f);


    }
}
